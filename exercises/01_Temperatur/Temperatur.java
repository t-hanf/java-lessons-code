public class Temperatur {

    public static void main(String[] args) {
        float c20 = 20f;
        float c30 = 30f;
        float c40 = 40f;
        float c120 = 120f;

        System.out.println(c20 * 9 / 5 + 32);
        System.out.println(c30 * 9 / 5 + 32);
        System.out.println(c40 * 9 / 5 + 32);
        System.out.println(c120 * 9 / 5 + 32);
    }

}
